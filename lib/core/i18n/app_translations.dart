class AppTranslations extends Translations {
  static const storageKey = 'app_language_code';

  static const Locale fallback = Locale('fr', 'FR');

  static final List<Locale> supportedLocales = <Locale>[
    const Locale('fr', 'FR'),
    const Locale('en', 'US'),
    const Locale('ln', 'CD'),
    const Locale('sw', 'CD'),
  ];

  static Locale getStoredLocale() {
    final storage = GetStorage(Setting.storageName);
    final code = storage.read(storageKey) as String?;
    return localeFromCode(code);
  }

  static Locale localeFromCode(String? code) {
    switch (code) {
      case 'en':
        return const Locale('en', 'US');
      case 'ln':
        return const Locale('ln', 'CD');
      case 'sw':
        return const Locale('sw', 'CD');
      case 'fr':
      default:
        return fallback;
    }
  }

  static String codeFromLocale(Locale locale) {
    return locale.languageCode;
  }

  static String labelFromCode(String code) {
    switch (code) {
      case 'en':
        return 'English';
      case 'ln':
        return 'Lingala';
      case 'sw':
        return 'Swahili';
      case 'fr':
      default:
        return 'Francais';
    }
  }

  static Future<void> changeLocale(String code) async {
    final locale = localeFromCode(code);
    final storage = GetStorage(Setting.storageName);
    await storage.write(storageKey, code);
    await Get.updateLocale(locale);
  }

